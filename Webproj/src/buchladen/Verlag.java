package buchladen;

public class Verlag {
	
	private int verlag_id;
	private String verlag_name;
	private String verlag_adresse;
	private String verlag_kontakt;
	
	
	public int getVerlag_id() {
		return verlag_id;
	}
	public void setVerlag_id(int verlag_id) {
		this.verlag_id = verlag_id;
	}
	public String getVerlag_name() {
		return verlag_name;
	}
	public void setVerlag_name(String verlag_name) {
		this.verlag_name = verlag_name;
	}
	public String getVerlag_adresse() {
		return verlag_adresse;
	}
	public void setVerlag_adresse(String verlag_adresse) {
		this.verlag_adresse = verlag_adresse;
	}
	public String getVerlag_kontakt() {
		return verlag_kontakt;
	}
	public void setVerlag_kontakt(String verlag_kontakt) {
		this.verlag_kontakt = verlag_kontakt;
	}

}
